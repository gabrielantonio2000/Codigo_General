const dia = 0;

switch (dia){
    case 0:
    console.log('Domingo');
    break;
    case 1: 
    console.log( 'Lunes');
    break;
    case 2:
    console.log('Martes');
    break;
    case 3: 
    console.log('Miercoles');
    default:
        console.log('No es domingo, lunes, martes o miercoles')
}